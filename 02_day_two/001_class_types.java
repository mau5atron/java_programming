class classTypes {
	/*
		In addition to the primitive darta types, a variable can have a class
		as its type, as in the following examples. 
	*/

		String lastName = "Hopper";
		Color hair;
		MarsRobot robbie;

	/*
		When a variable has a class as its type, the variabble refers to an
		object of that class of one of its subclasses.

		The last statement in the preceding list creates a variable named
		robbie tha is reserved for a MarsRobot object. You learn more 
		tomorrow about how to associate objects with variables. 
	*/ 
}